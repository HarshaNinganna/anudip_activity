def check_palindrome():
    string1 = "sms"
    string2 = "lms"  
    if string1 == string1[::-1]:
        print("string1 is a palindrome")
    else:
        print("string1 is not a palindrome") 
    if string2 == string2[::-1]:
        print("string2 is a palindrome")
    else:
        print("string2 is not a palindrome")
check_palindrome()

    
