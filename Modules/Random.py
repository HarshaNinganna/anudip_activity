import random as rnd
import math

print("Random Integer (1-100):", rnd.randint(1, 100))

sqroot_num = 49
sqrt_val = math.sqrt(sqroot_num)
print(f"Square root of {sqroot_num} is {sqrt_val}")

angle_deg = 90
angle_rad = math.radians(angle_deg)
sine_val = math.sin(angle_rad)
print(f"Sine of {angle_deg} degrees is {sine_val}")
